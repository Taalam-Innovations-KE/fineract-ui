import { spawn } from "node:child_process";
import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import ts from "typescript";

const generatedDir = path.resolve("src/lib/fineract/generated");
const generatedTypesPath = path.join(generatedDir, "types.gen.ts");
const generatedIndexPath = path.join(generatedDir, "index.ts");

const compatibilityAliases = [
	["ClientDataWritable", "ClientData"],
	["DeleteGlAccountsResponse", "DeleteGLAccountsResponse"],
	["GetBusinessDatesResponse", "BusinessDateResponse[]"],
	["GetGlAccountsResponse", "GetGLAccountsResponse"],
	["GetGlAccountsTemplateResponse", "GetGLAccountsTemplateResponse"],
	["GlAccountData", "GLAccountData"],
	["IsCatchUpRunningDto", "IsCatchUpRunningDTO"],
	["OldestCobProcessedLoanDto", "OldestCOBProcessedLoanDTO"],
	["PostGlAccountsRequest", "PostGLAccountsRequest"],
	["PostGlAccountsResponse", "PostGLAccountsResponse"],
	["PutGlAccountsRequest", "PutGLAccountsRequest"],
	["PutGlAccountsResponse", "PutGLAccountsResponse"],
	["PutJobsJobIdRequest", "PutJobsJobIDRequest"],
];

function stripGeneratedOperationFunctions(source) {
	const sourceFile = ts.createSourceFile(
		generatedTypesPath,
		source,
		ts.ScriptTarget.Latest,
		true,
		ts.ScriptKind.TS,
	);
	const removals = [];

	for (const statement of sourceFile.statements) {
		if (!ts.isVariableStatement(statement)) {
			continue;
		}

		const shouldRemove = statement.declarationList.declarations.every(
			(declaration) =>
				declaration.initializer &&
				(ts.isArrowFunction(declaration.initializer) ||
					ts.isFunctionExpression(declaration.initializer)),
		);

		if (shouldRemove) {
			removals.push([statement.getFullStart(), statement.end]);
		}
	}

	return removals
		.reverse()
		.reduce(
			(result, [start, end]) => `${result.slice(0, start)}${result.slice(end)}`,
			source,
		);
}

function run(command, args) {
	return new Promise((resolve, reject) => {
		const child = spawn(command, args, {
			stdio: "inherit",
			shell: false,
		});

		child.on("error", reject);
		child.on("close", (code) => {
			if (code === 0) {
				resolve();
				return;
			}

			reject(new Error(`${command} ${args.join(" ")} exited with ${code}`));
		});
	});
}

async function appendCompatibilityAliases() {
	const source = stripGeneratedOperationFunctions(
		(await readFile(generatedTypesPath, "utf8"))
			.replace(/ 🍺/g, "")
			.replace(
				/empty\?: boolean;\n\s+\[key: string\]: string\[\];/g,
				"empty?: boolean;\n  [key: string]: string[] | boolean | undefined;",
			)
			.replace(
				/empty\?: boolean;\n\s+\[key: string\]: ParameterizedHeader\[\];/g,
				"empty?: boolean;\n  [key: string]: ParameterizedHeader[] | boolean | undefined;",
			),
	);
	const missingTargets = compatibilityAliases
		.map(([, target]) => target.replace(/\[\]$/, ""))
		.filter(
			(target) =>
				!new RegExp(
					`export\\s+(interface|type|enum|const)\\s+${target}\\b`,
				).test(source),
		);

	if (missingTargets.length > 0) {
		throw new Error(
			`Orval output is missing compatibility alias targets: ${missingTargets.join(", ")}`,
		);
	}

	const aliases = compatibilityAliases
		.map(([alias, target]) => `export type ${alias} = ${target};`)
		.join("\n");

	await writeFile(
		generatedTypesPath,
		`${source.trimEnd()}\n\n// Compatibility aliases retained for existing Hey API generated SDK imports.\n${aliases}\n`,
	);
}

async function writeIndexBarrel() {
	await writeFile(
		generatedIndexPath,
		'// This file is generated by scripts/generate-fineract-api.mjs.\nexport * from "./types.gen";\n',
	);
}

async function walkSourceFiles(directory, files = []) {
	const entries = await readdir(directory, { withFileTypes: true });

	for (const entry of entries) {
		const entryPath = path.join(directory, entry.name);
		if (entry.isDirectory()) {
			if (entryPath === generatedDir || entry.name === ".next") {
				continue;
			}
			await walkSourceFiles(entryPath, files);
			continue;
		}

		if (entry.isFile() && /\.(ts|tsx)$/.test(entry.name)) {
			files.push(entryPath);
		}
	}

	return files;
}

function isGeneratedImport(modulePath, sourceFilePath) {
	if (
		modulePath === "@/lib/fineract/generated" ||
		modulePath === "@/lib/fineract/generated/types.gen"
	) {
		return true;
	}

	if (!modulePath.startsWith(".")) {
		return false;
	}

	const resolved = path.resolve(path.dirname(sourceFilePath), modulePath);
	const generatedIndex = path.join(generatedDir, "index.ts");
	const candidates = [
		resolved,
		`${resolved}.ts`,
		path.join(resolved, "index.ts"),
	];

	return (
		candidates.includes(generatedDir) ||
		candidates.includes(generatedTypesPath) ||
		candidates.includes(generatedIndex)
	);
}

function collectGeneratedImports(sourceText, sourceFilePath) {
	const sourceFile = ts.createSourceFile(
		sourceFilePath,
		sourceText,
		ts.ScriptTarget.Latest,
		true,
		sourceFilePath.endsWith(".tsx") ? ts.ScriptKind.TSX : ts.ScriptKind.TS,
	);
	const names = [];

	for (const statement of sourceFile.statements) {
		if (
			!ts.isImportDeclaration(statement) ||
			!ts.isStringLiteral(statement.moduleSpecifier) ||
			!isGeneratedImport(statement.moduleSpecifier.text, sourceFilePath)
		) {
			continue;
		}

		const namedBindings = statement.importClause?.namedBindings;
		if (!namedBindings || !ts.isNamedImports(namedBindings)) {
			continue;
		}

		for (const element of namedBindings.elements) {
			names.push((element.propertyName ?? element.name).text);
		}
	}

	return names;
}

function collectGeneratedExports(sourceText) {
	return new Set(
		[
			...sourceText.matchAll(
				/export\s+(?:interface|type|enum|const|function|class)\s+([A-Za-z_$][\w$]*)/g,
			),
		].map((match) => match[1]),
	);
}

async function assertGeneratedImportsResolve() {
	const files = await walkSourceFiles(path.resolve("src"));
	const importedNames = new Set();

	for (const filePath of files) {
		const sourceText = await readFile(filePath, "utf8");
		for (const name of collectGeneratedImports(sourceText, filePath)) {
			importedNames.add(name);
		}
	}

	const generatedSource = await readFile(generatedTypesPath, "utf8");
	const exportedNames = collectGeneratedExports(generatedSource);
	const missingNames = [...importedNames]
		.filter((name) => !exportedNames.has(name))
		.sort();

	if (missingNames.length > 0) {
		throw new Error(
			`Generated Fineract SDK is missing imported names: ${missingNames.join(", ")}`,
		);
	}

	console.log(
		`Verified ${importedNames.size} generated Fineract SDK imports still resolve.`,
	);
}

async function main() {
	const pnpm = process.platform === "win32" ? "pnpm.cmd" : "pnpm";

	await run(pnpm, ["exec", "orval", "--config", "orval.config.mjs"]);
	await appendCompatibilityAliases();
	await writeIndexBarrel();
	await assertGeneratedImportsResolve();
}

main().catch((error) => {
	console.error(error);
	process.exitCode = 1;
});
